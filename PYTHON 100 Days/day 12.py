#printng strings // in split
names = "Shriram,Atharv,Shubham,Harry"
print("size of the string is",len(names))       #get length of a string using len()
print(names)            #prints full string
print(names[0:6])       #prints string from 0 to 5      #6 dose not print
print(names[:6])        #automatically takes 0 before :
print(names[:])         #automatically prints full string
print(names[5:-10])     #starts printing from 5 and prints till -10 from last numbers
